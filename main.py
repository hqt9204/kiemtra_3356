# sum a,b
def sum(a, b):
    return a + b
if __name__ == "__main__":
    a = int(input("Nhập số a: "))
    b = int(input("Nhập số b: "))
    print("Tổng của a và b là:", sum(a, b))

# sub a,b
def sub(a, b):
    return a - b
if __name__ == "__main__":
    a = int(input("Nhập số a: "))
    b = int(input("Nhập số b: "))
    print("Hiệu của a và b là:", sub(a, b))